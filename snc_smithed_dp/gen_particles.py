"""Generate a particles.mcfunction with hardcoded particle points for a shape.

Usage:
    python gen_particles.py --shape sphere --particle heart --size 4
    python gen_particles.py --shape circle --particle flame --size 3 --density 32
    python gen_particles.py --shape cube   --particle end_rod --size 2
    python gen_particles.py --shape cube --particle end_rod --size 2 --density 120 --force --viewers @s --output data\snc\function\dynamic\elevator\particles.mcfunction

The generated function places one particle per computed point using relative
coordinates, so it plays centered on whatever executes it:
    particle minecraft:heart ~1.5 ~ ~-0.7 0 0 0 0 1 force
"""

import argparse
import math

SHAPES = ("sphere", "circle", "cube")


def fmt(value):
    """Format an offset as a Minecraft relative coordinate (~ for zero)."""
    value = round(value, 4)
    if value == 0:
        return "~"
    # Trim trailing zeros while keeping it a valid literal.
    text = f"{value:.4f}".rstrip("0").rstrip(".")
    return f"~{text}"


def circle_points(size, density):
    """Points evenly spaced around a circle of radius `size` in the XZ plane."""
    points = []
    for i in range(density):
        angle = 2 * math.pi * i / density
        points.append((size * math.cos(angle), 0.0, size * math.sin(angle)))
    return points


def sphere_points(size, density):
    """Points spread over a sphere surface of radius `size` (Fibonacci sphere)."""
    points = []
    golden = math.pi * (3 - math.sqrt(5))  # golden angle
    for i in range(density):
        y = 1 - (i / (density - 1)) * 2 if density > 1 else 0
        radius = math.sqrt(max(0.0, 1 - y * y))
        theta = golden * i
        x = math.cos(theta) * radius
        z = math.sin(theta) * radius
        points.append((size * x, size * y, size * z))
    return points


def cube_points(size, density):
    """Points along the 12 edges of a cube with half-side `size`."""
    per_edge = max(2, density // 12)
    corners = [
        (sx, sy, sz)
        for sx in (-size, size)
        for sy in (-size, size)
        for sz in (-size, size)
    ]
    # Edges connect corners that differ in exactly one axis.
    edges = []
    for i, a in enumerate(corners):
        for b in corners[i + 1:]:
            if sum(1 for ca, cb in zip(a, b) if ca != cb) == 1:
                edges.append((a, b))

    points = []
    for a, b in edges:
        for step in range(per_edge):
            t = step / (per_edge - 1)
            points.append(tuple(a[k] + (b[k] - a[k]) * t for k in range(3)))
    return points


GENERATORS = {
    "sphere": sphere_points,
    "circle": circle_points,
    "cube": cube_points,
}


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--shape", required=True, choices=SHAPES,
                        help="shape to trace with particles")
    parser.add_argument("--particle", required=True,
                        help="particle id (e.g. heart, flame, end_rod)")
    parser.add_argument("--size", type=float, required=True,
                        help="radius / half-side of the shape, in blocks")
    parser.add_argument("--density", type=int, default=None,
                        help="number of points (default depends on shape)")
    parser.add_argument("--output", default="particles.mcfunction",
                        help="output file path (default: particles.mcfunction)")
    parser.add_argument("--force", action="store_true",
                        help="use display mode 'force' (visible far away / when off); "
                             "otherwise 'normal'")
    parser.add_argument("--viewers", default=None,
                        help="target selector for who sees the particles (e.g. @s). "
                             "Omit to show to everyone (@a)")
    args = parser.parse_args()

    display = "force" if args.force else "normal"
    if args.viewers:
        display = f"{display} {args.viewers}"

    density = args.density
    if density is None:
        density = {"circle": 32, "sphere": 100, "cube": 96}[args.shape]

    particle = args.particle
    if ":" not in particle:
        particle = f"minecraft:{particle}"

    points = GENERATORS[args.shape](args.size, density)

    lines = [
        f"# Auto-generated by gen_particles.py",
        f"# shape={args.shape} particle={particle} size={args.size} density={density} mode={display}",
        "",
    ]
    for x, y, z in points:
        lines.append(
            f"particle {particle} {fmt(x)} {fmt(y)} {fmt(z)} 0 0 0 0 1 {display}"
        )

    with open(args.output, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")

    print(f"Wrote {len(points)} particles to {args.output}")


if __name__ == "__main__":
    main()

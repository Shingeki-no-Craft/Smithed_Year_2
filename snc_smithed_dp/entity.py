"""
Reads the booth schematics and writes a single mcfunction that summons every
entity in them, positioned relative to where the function is run.

The booth was split across several .schem files. Each entity is placed at
(schematic Offset + entity Pos) so that all the files reassemble around one
common anchor -- the WorldEdit origin you'd //paste them from.

Output: data/snc/function/summit/summon.mcfunction  (snc:summit/summon)

Run it, then in-game stand where the booth's origin should be and run:
    /function snc:summit/summon
"""

import os

import nbtlib
from nbtlib import serialize_tag

SCHEM_PATHS = ["snc_booth.schem"]
OUT_PATH = os.path.join(
    "data", "snc", "function", "summit", "summon.mcfunction"
)

# Keys inside an entity's NBT that must NOT be re-summoned.
#   id  -> would be redundant with the summon <id> argument
#   Pos -> absolute world coords; would override the relative ~ ~ ~ position
# UUID is kept so entities keep the stable IDs stored in the schematic.
# (Set KEEP_UUID = False to let the game assign fresh random UUIDs instead.)
KEEP_UUID = True
STRIP_KEYS = ("id", "Pos", "UUIDMost", "UUIDLeast")
if not KEEP_UUID:
    STRIP_KEYS = STRIP_KEYS + ("UUID",)


def get_schematic(nbt_file):
    """Return the schematic compound.

    Handles the several ways the root can be wrapped:
      * unnamed root compound: nbt_file[''] -> {'Schematic': ...}   (WorldEdit v3)
      * 'Schematic' directly at the top level                       (some exports)
      * a flat root that already is the schematic
    """
    root = nbt_file
    if "" in root:  # unwrap the unnamed root compound
        root = root[""]
    if "Schematic" in root:
        return root["Schematic"]
    return root


def get_entities(schem):
    for key in ("Entities", "entities"):
        if key in schem:
            return schem[key]
    return []


def get_offset(schem):
    """Return the schematic's (x, y, z) offset from the paste origin."""
    off = schem.get("Offset")
    if off is None:
        return (0.0, 0.0, 0.0)
    return (float(off[0]), float(off[1]), float(off[2]))


def fix_empty_text_keys(tag):
    """Rename any "" key to "text" (text components stored with an empty key)."""
    if isinstance(tag, dict):
        if "" in tag and "text" not in tag:
            tag["text"] = tag.pop("")
        for value in tag.values():
            fix_empty_text_keys(value)
    elif isinstance(tag, list):
        for value in tag:
            fix_empty_text_keys(value)
    return tag


def fmt_coord(value):
    """Format a relative coordinate with a ~ prefix, trimming trailing zeros."""
    text = f"{float(value):.5f}".rstrip("0").rstrip(".")
    if text in ("", "-0"):
        text = "0"
    return f"~{text}"


def entity_command(entity, offset=(0.0, 0.0, 0.0)):
    ent_id = str(entity.get("id") or entity.get("Id"))

    pos = entity.get("Pos")
    if pos is None:
        px = py = pz = 0.0
    else:
        px, py, pz = (float(pos[0]), float(pos[1]), float(pos[2]))
    # Add the schematic offset so every split file aligns to one shared anchor.
    px += offset[0]
    py += offset[1]
    pz += offset[2]
    coords = f"{fmt_coord(px)} {fmt_coord(py)} {fmt_coord(pz)}"

    # The entity's full NBT lives either in a nested "Data"/"nbt" compound
    # (Sponge v3) or directly on the entity compound.
    data = entity.get("Data")
    if data is None:
        data = entity.get("nbt")
    if data is None:
        data = nbtlib.Compound(entity)

    data = nbtlib.Compound(data)
    for key in STRIP_KEYS:
        data.pop(key, None)
    fix_empty_text_keys(data)

    nbt = serialize_tag(data, compact=True) if data else ""
    # compact=True emits no structural newlines, so any raw control character
    # here lives inside a quoted string (e.g. a text_display with blank lines).
    # Escape them or they split the command across lines and break the function.
    nbt = nbt.replace("\r\n", "\\n").replace("\n", "\\n")
    nbt = nbt.replace("\r", "\\n").replace("\t", "\\t")
    return f"summon {ent_id} {coords} {nbt}".rstrip()


def main():
    lines = [
        f"# Generated by entity.py from {', '.join(SCHEM_PATHS)}",
        "# Summons every entity from the schematics, relative to the run position.",
        "",
    ]

    total = 0
    for path in SCHEM_PATHS:
        if not os.path.exists(path):
            print(f"Skipping missing file: {path}")
            continue

        schem = get_schematic(nbtlib.load(path))
        entities = get_entities(schem)
        offset = get_offset(schem)

        lines.append(f"# --- {path}: {len(entities)} entities ---")
        for entity in entities:
            lines.append(entity_command(entity, offset))
        total += len(entities)

    os.makedirs(os.path.dirname(OUT_PATH), exist_ok=True)
    with open(OUT_PATH, "w", encoding="utf-8") as handle:
        handle.write("\n".join(lines) + "\n")

    print(f"Wrote {total} entit{'y' if total == 1 else 'ies'} to {OUT_PATH}")


if __name__ == "__main__":
    main()
